"""
Example of methods for both built-in datatypes
"""

# Example 1 - method on an int
num = 13
print(num.bit_count())


# Example 2 - method on a boolean
is_awesome = True
print(is_awesome.bit_count())


# Example 3 - method on a string
name = "Niels"
print(name.upper())


# Example 4 - method on a data structure
groceries = ["apples", "bread", "butter"]
groceries.append("beer")
